function displayImgs(exposure)
{
    var expo = "Exposure: ";
    switch(exposure)
    {
        case 0:
            expo += "0";
            break;
        case 1:
            expo += "1";
            break;
        case 2:
            expo += "2";
            break;
        case 3:
            expo += "-1";
            break;
        case 4:
            expo += "-2";
        default:
            break;
    }
    document.getElementById("exp").innerHTML = expo;
    switch (set) 
    {
        case 1:
            document.getElementById("raw").src = set1s[exposure].raw;
            //console.log(set1s[exposure].raw);
            document.getElementById("jpeg").src = set1s[exposure].jpeg;
            //console.log(set1s[exposure].jpeg);
            switchToLargerImgs(set1[exposure].raw, set1[exposure].jpeg);
            break;
        case 2:
            document.getElementById("raw").src = set2s[exposure].raw;
            document.getElementById("jpeg").src = set2s[exposure].jpeg;
            switchToLargerImgs(set2[exposure].raw, set2[exposure].jpeg);
            break;
        case 3:
            document.getElementById("raw").src = set3s[exposure].raw;
            document.getElementById("jpeg").src = set3s[exposure].jpeg;
            switchToLargerImgs(set3[exposure].raw, set3[exposure].jpeg);
            break;
        case 4:
            document.getElementById("raw").src = set4s[exposure].raw;
            document.getElementById("jpeg").src = set4s[exposure].jpeg;
            switchToLargerImgs(set4[exposure].raw, set4[exposure].jpeg);
            break;
        case 5:
            document.getElementById("raw").src = set5s[exposure].raw;
            document.getElementById("jpeg").src = set5s[exposure].jpeg;
            switchToLargerImgs(set5[exposure].raw, set5[exposure].jpeg);
            break;
        case 6:
            document.getElementById("raw").src = set6s[exposure].raw;
            document.getElementById("jpeg").src = set6s[exposure].jpeg;
            switchToLargerImgs(set6[exposure].raw, set6[exposure].jpeg);
            break;
        default:
            break;
    }
    ex = exposure;
}

function setInfo()
{
    document.getElementById("imgSet").innerHTML = "Image Set: " + set;
}

function switchToLargerImgs(rawSrc, jpegSrc)
{
    var rawImg = new Image();
    var jpegImg = new Image();
    rawImg.src = rawSrc;
    jpegImg.src = jpegSrc;
    rawImg.onload = function()
    {
        document.getElementById("raw").src = rawImg.src;
    }
    jpegImg.onload = function()
    {
        document.getElementById("jpeg").src = jpegImg.src;
    }
}